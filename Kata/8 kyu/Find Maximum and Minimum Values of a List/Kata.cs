namespace codewars.com.Kata._8_kyu.Find_Maximum_and_Minimum_Values_of_a_List
{
    using System.Linq;

    public class Kata
    {
        public int Min(int[] list)
        {
            return list.Min();
        }

        public int Max(int[] list)
        {
            return list.Max();
        }
    }
}
// Convert a Boolean to a String
// https://www.codewars.com/kata/551b4501ac0447318f0009cd

namespace codewars.com.Kata._8_kyu.Convert_a_Boolean_to_a_String
{
    public class kata
    {
        public static string boolean_to_string(bool b)
        {
            return (b) ? "True" : "False";
        }
    }
}
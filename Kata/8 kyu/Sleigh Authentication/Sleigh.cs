namespace codewars.com.Kata._8_kyu.Sleigh_Authentication
{
    public class Sleigh
    {
        public static bool Authenticate(string name, string password)
        {
            return name == "Santa Claus" && password == "Ho Ho Ho!";
        }
    }
}